main() {
  // Operadores de igualdade e relacional.
  bool resultado = 1 > 3; // Maior que
  print(resultado);

  resultado = 1 < 3; // Menor que
  print(resultado);

  resultado = 1 >= 3; // Maior que ou igual
  print(resultado);

  resultado = 1 <= 3; // Menor que ou igual
  print(resultado);

  resultado = 1 == 3; // igualdade
  print(resultado);

  resultado = 1 != 3; // diferente
  print(resultado);
}
